package com.osmanbatuhankalkan._1_part_javatypes;

public class _04_operators {
    public static void main(String[] args) {
        //Operator
        //+
        //-
        //*
        // /
        // %
        // ++
        // --
        int number1 = 20,number2 = 10;
        int sum = number1 + number2;
        int subtraction = number1 - number2;
        int division = (number1 / number2);
        int multiplication = (number1 * number2);
        int modulus = (number1 % number2);
        System.out.println("Toplam: "+number1+" + "+number2+" = "+sum);
        System.out.println("Çıkarma: "+number1+" - "+number2+" = "+subtraction);
        System.out.println("Bölme: "+number1+" / "+number2+" = "+division);
        System.out.println("Çarpma: "+number1+" * "+number2+" = "+multiplication);
        System.out.println("Kalan: "+number1+" * "+number2+" = "+modulus);

        //Mantıksa Operators: && = VE || = VEYA
    }
}
